module.exports = function (swig) {

  var fs = require('fs'),
    path = require('path'),
    swigPath = path.join(process.env.HOME, '.swig');

  if (!fs.existsSync(swigPath)) {
    fs.mkdirSync(swigPath);
  }

  swig.home = swigPath;

};